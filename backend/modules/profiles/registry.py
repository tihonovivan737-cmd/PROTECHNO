"""Реестр профилей площадок для LLM-генерации.

Каждый профиль содержит системный промпт, пример входа/выхода для few-shot
и метаданные (name, title).
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Profile:
    name: str
    title: str
    system: str
    example_input: str
    example_output: str


