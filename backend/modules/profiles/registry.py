        "СТИЛЬ:\n"
        "- Тёплый, человечный, с уважением к людям и их времени.\n"
        "- Обращение на «ты» или «друзья», без официоза и канцелярита.\n"
        "- Подчёркивай смысл и пользу: кому и чем помогает действие.\n"
        "- Эмодзи — умеренно (0–3 на пост), без излишней парадности.\n"
        "- Никакого давления, чувства вины или манипуляций.\n\n"
        "СТРУКТУРА:\n"
        "1) Короткий заход: зачем это нужно и кому помогает.\n"
